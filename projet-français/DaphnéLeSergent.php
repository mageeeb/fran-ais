            <p> Le passé est souvent considéré comme une période révolue, mais il peut résonner dans le présent. C'est ce que montrent les événements actuels, qui ont leurs racines dans le passé. En adoptant un point de vue narratif de type journalistique, nous pouvons donner vie à ces histoires, même si elles ont déjà été racontées. <br>

Lorsqu'on regarde en arrière, « ça a été » est souvent utilisé pour décrire les événements qui ont déjà eu lieu. Cependant, ces événements peuvent avoir une influence sur le présent. Cela est vrai pour l'histoire de notre pays, notre ville, notre communauté et même notre propre vie. <br>

Prenons l'exemple de la pandémie actuelle. Nous pourrions dire « ça a été » pour parler de la grippe espagnole de 1918, mais cette pandémie a influencé la manière dont les gouvernements et les individus ont réagi à la pandémie de COVID-19. La pandémie actuelle a également des répercussions sur la façon dont nous vivons et interagissons les uns avec les autres. <br>

En regardant de plus près, nous pouvons voir comment les événements passés ont façonné notre présent. Les conflits, les guerres, les mouvements sociaux, les découvertes scientifiques et les avancées technologiques ont tous des conséquences durables. Nous pouvons voir comment l'histoire s'entrelace avec le présent et comment elle façonne notre avenir. <br>

Prenons l'exemple de l'évolution des droits des femmes. Dans le passé, les femmes étaient souvent considérées comme inférieures aux hommes et leurs droits étaient limités. Cependant, grâce aux mouvements de défense des droits des femmes, les femmes ont gagné des droits tels que le droit de vote et l'égalité des salaires. Ces changements ont eu des répercussions sur la société actuelle, mais il reste encore beaucoup à faire pour atteindre l'égalité des sexes. <br>

De même, l'avènement de la technologie a eu un impact significatif sur notre monde. Les avancées dans les domaines de l'informatique, des télécommunications et de l'automatisation ont radicalement changé notre façon de vivre et de travailler. Les technologies de pointe ont également des conséquences sur notre santé, notre sécurité et notre environnement. <br>
 </p>
           
